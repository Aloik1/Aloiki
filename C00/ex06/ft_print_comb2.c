#include <unistd.h>

void ft_print_comb2(void)
{
	char	a, a1, b, b1;

	a1 = '0';
	while (a1 <= '8')
	{
		a = '0';
		while (a <= '9')
		{
			b = '0';
			while (b <= '9')
			{
				b1 = a1 + 1;
				while (b1 <= '9')
				{
					write (1, &a, 1);
					write (1, &a1, 1);
					write (1, " ", 1);
					write (1, &b, 1);
					write (1, &b1, 1);
					write (1, ", ", 2);
					b1++;
				}
				b++;
			}
			a1++;
		}
		a++;
	}
}

int	main(void)
{
	ft_print_comb2();
}
